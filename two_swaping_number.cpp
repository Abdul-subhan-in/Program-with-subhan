#include<iostream>
using namespace std;
int main()
{
    //12: Swapping two number (Without logic)
    int p = 5,q = 10;
    //before swapping: a = 5 and b =10
    cout << "Before swapping a = "<< p <<", b = "<< q << endl;
    // After swapping: a = 10 and b = 5
    p = 10, q = 5;
    cout << "After swapping a = "<< p <<", b = "<< q <<endl;
    return 0;
} 
// Output
// Before swapping a = 5, b = 10
// After swapping a = 10, b = 5